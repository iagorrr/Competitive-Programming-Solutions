template <typename T1, typename T2>
constexpr typename std::common_type<T1, T2>::type floor_div(T1 x, T2 y) {
  assert(y != 0);
  if (y < 0) x = -x, y = -y;
  return x < 0 ? (x - y + 1) / y : x / y;
}

int32_t main(){
  ios_base::sync_with_stdio(0);
  cin.tie(0);
  ll x0,y0,r;
  x0=read();
  y0=read();
  r=read();
  ll ans=0;
  const ll R=2'00'000;
  for(ll i=-R;i<=R;i++){
    ll y=i*f;
    ll dy=y0-y;
    ll dx2=r*r-dy*dy;
    if(dx2<0)continue;
    ll dx=floor(sqrt((long double)dx2));
    ans += floor_div(x0+dx,f) - floor_div(x0-dx-1,f);
  }
  cout<<ans<<'\n';
}
